        public void Update(TEntity entity)
        {
            Context.Entry<TEntity>(entity).State = EntityState.Modified;
        }

        public void Save()
        {
            this.Context.SaveChanges();
        }

        public Task<int> SaveAsync()
        {
            return this.Context.SaveChangesAsync();
        }

